def main(digits):
    max_product = 0
    max_window = ""
    sourceLength = len(SOURCE)

    for n in range(sourceLength):
        if n > sourceLength - digits:
            break

        currentWindow = SOURCE[n: n + digits]
        # print(f"currentWindow is {currentWindow}")
        current_product = 1

        for i in currentWindow:
            num = int(i)
            current_product *= num
            if (num == 0):
                break

        max_product = max(current_product, max_product)
        if current_product == max_product:
            max_window = currentWindow

    print(f"{digits} digits returns {max_window}")
    return max_product

